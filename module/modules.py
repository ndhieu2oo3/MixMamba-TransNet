class GatedCNNBlock(nn.Module):
    r""" Our implementation of Gated CNN Block: https://arxiv.org/pdf/1612.08083
    Args:
        conv_ratio: control the number of channels to conduct depthwise convolution.
            Conduct convolution on partial channels can improve paraitcal efficiency.
            The idea of partial channels is from ShuffleNet V2 (https://arxiv.org/abs/1807.11164) and
            also used by InceptionNeXt (https://arxiv.org/abs/2303.16900) and FasterNet (https://arxiv.org/abs/2303.03667)
    """
    def __init__(self, dim, expansion_ratio=8/3, kernel_size=7, conv_ratio=1.0,dilation =1,
                 norm_layer=partial(nn.LayerNorm,eps=1e-6),
                 act_layer=nn.GELU,
                 drop_path=0.,
                 **kwargs):
        super().__init__()
        self.norm = norm_layer(dim)
        hidden = int(expansion_ratio * dim)
        self.fc1 = nn.Linear(dim, hidden * 2)
        self.act = act_layer()
        conv_channels = int(conv_ratio * dim)
        self.split_indices = (hidden, hidden - conv_channels, conv_channels)
        self.conv1 = nn.Conv2d(conv_channels, conv_channels, kernel_size=3, padding='same', groups=conv_channels)
        self.conv2 = nn.Conv2d(conv_channels, conv_channels, kernel_size=5, padding='same', groups=conv_channels)
        self.conv3 = nn.Conv2d(conv_channels, conv_channels, kernel_size=7, padding='same', groups=conv_channels)
        self.fc2 = nn.Linear(hidden, dim)
        self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()

    def forward(self, x):
        shortcut = x # [B, H, W, C]
        x = self.norm(x)
        g, i, c = torch.split(self.fc1(x), self.split_indices, dim=-1)
        c = c.permute(0, 3, 1, 2) # [B, H, W, C] -> [B, C, H, W]
        c1 = self.conv1(c)
        c2 = self.conv2(c)
        c3 = self.conv3(c)
        c = c1 + c2 + c3
        c = c.permute(0, 2, 3, 1) # [B, C, H, W] -> [B, H, W, C]
        x = self.fc2(self.act(g) * torch.cat((i, c), dim=-1))
        x = self.drop_path(x)
        return x + shortcut

class SkipConnection(nn.Module):

  def __init__(self, dim):
    super().__init__()
    self.block = GatedCNNBlock(dim)
  def forward(self, x):
    x = x.permute(0, 2, 3, 1)
    x = self.block(x)
    x = x.permute(0, 3, 1, 2)
    return x
  
# Mamba Mixer
class Mlp2(nn.Module):
    """ MLP as used in MetaFormer models, eg Transformer, MLP-Mixer, PoolFormer, MetaFormer baslines and related networks.
    Mostly copied from timm.
    """
    def __init__(self, dim, mlp_ratio=4, out_features=None, act_layer=nn.GELU, drop=0., bias=False, **kwargs):
        super().__init__()
        in_features = dim
        out_features = out_features or in_features
        hidden_features = int(mlp_ratio * in_features)
        drop_probs = to_2tuple(drop)

        self.fc1 = nn.Linear(in_features, hidden_features, bias=bias)
        self.act = act_layer()
        self.drop1 = nn.Dropout(drop_probs[0])
        self.fc2 = nn.Linear(hidden_features, out_features, bias=bias)
        self.drop2 = nn.Dropout(drop_probs[1])

    def forward(self, x):
        x = self.fc1(x)
        x = self.act(x)
        x = self.drop1(x)
        x = self.fc2(x)
        x = self.drop2(x)
        return x

class MambaMLPMixer(nn.Module):
  def __init__(self,dim,mlp=Mlp2,norm_layer=nn.LayerNorm,
                 drop=0., drop_path=0.1,):
    super().__init__()

    self.norm = norm_layer(dim)
    self.mlp = mlp(dim=dim,drop=drop)
    self.drop_path = DropPath(drop_path) if drop_path > 0. else nn.Identity()
    self.mamba = VSSBlock(dim)
    self.ins_norm = nn.InstanceNorm2d(dim, affine=True)
    self.act = nn.LeakyReLU(negative_slope=0.01)
  def forward(self,x):
    residual = x
    x = x.permute(0, 2, 3, 1) # (B, C, H, W) -> (B, H, W, C)
    x = self.norm(self.mamba(x))
    x = x.permute(0, 3, 1, 2) #  (B, H, W, C) -> (B, C, H, W)
    x = self.act(self.ins_norm(x))
    x = x + residual
    x = x.permute(0, 2, 3, 1) # (B, C, H, W) -> (B, H, W, C)
    x = x + self.drop_path(self.mlp(self.norm(x)))
    x = x.permute(0, 3, 1, 2) #  (B, H, W, C) -> (B, C, H, W)
    return x

